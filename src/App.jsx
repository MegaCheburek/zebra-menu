function Card({ item }) {
  return (
    <article className="menu-card">
      <div className="card-glow" />
      <div className="card-top">
        <div>
          <h3>{item.name}</h3>
          <span className="tag">{item.tag}</span>
        </div>
        <span className="mini-mark">Z</span>
      </div>
      <p>{item.text}</p>
    </article>
  )
}

export default function App() {
  const [active, setActive] = useState('coffee')
  const [query, setQuery] = useState('')
  const activeGroup = groups.find((g) => g.id === active) || groups[0]

  const filtered = useMemo(() => {
    const q = query.trim().toLowerCase()
    return menu.filter((item) => item.group === active && (!q || `${item.name} ${item.tag} ${item.text}`.toLowerCase().includes(q)))
  }, [active, query])

  const totalInGroup = menu.filter((item) => item.group === active).length

  return (
    <div className="app">
      <div className="zebra-bg" />

      <header className="hero">
        <div className="hero-inner">
          <div className="hero-copy">
            <div className="eyebrow"><span /> Zebra Coffee Menu Guide</div>
            <h1>Вкусно. Коротко. Запоминается.</h1>
            <p>Глоссарий по напиткам, еде и десертам. Каждое описание звучит как готовая красивая фраза для гостя — без сухой зубрёжки.</p>
          </div>

          <aside className="stats-card">
            <div className="stats-icon">{activeGroup.icon}</div>
            <div>
              <span>Открыта вкладка</span>
              <strong>{activeGroup.label}</strong>
            </div>
            <div className="stats-grid">
              <div><span>Всего</span><b>{totalInGroup}</b></div>
              <div><span>Показано</span><b>{filtered.length}</b></div>
            </div>
          </aside>
        </div>
      </header>

      <main className="container">
        <section className="toolbar">
          <nav className="tabs" aria-label="Категории меню">
            {groups.map((group) => (
              <button
                key={group.id}
                className={active === group.id ? 'tab active' : 'tab'}
                onClick={() => setActive(group.id)}
              >
                <span>{group.icon}</span>{group.label}
              </button>
            ))}
          </nav>

          <label className="search">
            <span>⌕</span>
            <input
              value={query}
              onChange={(e) => setQuery(e.target.value)}
              placeholder="Найти вкус, напиток или десерт..."
            />
          </label>
        </section>

        <section className="section-head">
          <div className="section-icon">{activeGroup.icon}</div>
          <div>
            <h2>{activeGroup.label}</h2>
            <p>Выбирай карточку и учи как готовую фразу.</p>
          </div>
        </section>

        {filtered.length > 0 ? (
          <section className="grid">
            {filtered.map((item) => <Card key={`${item.group}-${item.name}`} item={item} />)}
          </section>
        ) : (
          <section className="empty">
            <h3>Ничего не найдено</h3>
            <p>Очисти поиск или попробуй другое слово.</p>
          </section>
        )}
      </main>
    </div>
  )
}
